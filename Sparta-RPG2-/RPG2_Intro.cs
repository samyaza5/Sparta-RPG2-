            width += IsFullWidth(c) ? 2 : 1;
        }
        return width;
    }

    private static bool IsFullWidth(char c)
    {
        return c >= 0x1100 &&
               (c <= 0x115F ||
                c == 0x2329 || c == 0x232A ||
                (c >= 0x2E80 && c <= 0xA4CF && c != 0x303F) ||
                (c >= 0xAC00 && c <= 0xD7A3) ||
                (c >= 0xF900 && c <= 0xFAFF) ||
                (c >= 0xFE10 && c <= 0xFE19) ||
                (c >= 0xFF01 && c <= 0xFF60) ||
                (c >= 0xFFE0 && c <= 0xFFE6));
    }
